#!/usr/bin/env python3
"""
prepare_detection_dataset.py
============================
Prepare a YOLOv10-ready DETECTION dataset from the downloaded Plate_v4 export.

The Plate_v4 dataset labels plates with 29 *province* classes. For a plate
DETECTOR we only care about one thing: "is there a plate here?" So this script:

  1. Copies Plate_v4's train/valid/test images + labels into data/annotated/.
  2. Remaps ALL 29 province class ids -> a single class 0 = "license_plate".
  3. Writes a fresh data.yaml pointing at the copied folders (nc: 1).

After running, data/annotated/ is ready to hand straight to YOLOv10 training.

Usage
-----
    python scripts/prepare_detection_dataset.py

    # if your Plate_v4 folder is elsewhere:
    python scripts/prepare_detection_dataset.py --src "C:/path/to/Plate_v4.v3i.yolov8"

    # keep the original 29 province classes instead of collapsing to 1:
    python scripts/prepare_detection_dataset.py --keep-classes
"""

from __future__ import annotations

import argparse
import shutil
from pathlib import Path

# Project root = parent of the scripts/ folder this file lives in.
PROJECT_ROOT = Path(__file__).resolve().parent.parent
DEFAULT_SRC = Path(
    r"C:/Users/TUF/OneDrive/Pictures/Downloads/Plate_v4.v3i.yolov8"
)
DEST = PROJECT_ROOT / "data" / "annotated"

SPLITS = ["train", "valid", "test"]
IMG_EXTS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}
SINGLE_CLASS_NAME = "license_plate"


def remap_label_file(src_txt: Path, dst_txt: Path, keep_classes: bool) -> int:
    """Copy a YOLO label file, forcing every class id to 0 unless keep_classes.

    Returns the number of boxes written.
    """
    lines_out = []
    if src_txt.exists():
        for raw in src_txt.read_text().splitlines():
            raw = raw.strip()
            if not raw:
                continue
            parts = raw.split()
            if len(parts) < 5:
                continue  # malformed line, skip
            if not keep_classes:
                parts[0] = "0"
            lines_out.append(" ".join(parts))
    # Write (may be empty -> a valid "no objects" label).
    dst_txt.write_text("\n".join(lines_out) + ("\n" if lines_out else ""))
    return len(lines_out)


def process_split(src_root: Path, split: str, keep_classes: bool) -> tuple[int, int]:
    """Copy images + remapped labels for one split. Returns (imgs, boxes)."""
    src_img_dir = src_root / split / "images"
    src_lbl_dir = src_root / split / "labels"
    if not src_img_dir.is_dir():
        print(f"  [skip] no '{split}' split found in source.")
        return 0, 0

    dst_img_dir = DEST / split / "images"
    dst_lbl_dir = DEST / split / "labels"
    dst_img_dir.mkdir(parents=True, exist_ok=True)
    dst_lbl_dir.mkdir(parents=True, exist_ok=True)

    n_imgs = 0
    n_boxes = 0
    for img in sorted(src_img_dir.iterdir()):
        if img.suffix.lower() not in IMG_EXTS:
            continue
        # Copy image.
        shutil.copy2(img, dst_img_dir / img.name)
        # Copy + remap its label (label shares the image stem).
        src_txt = src_lbl_dir / f"{img.stem}.txt"
        dst_txt = dst_lbl_dir / f"{img.stem}.txt"
        n_boxes += remap_label_file(src_txt, dst_txt, keep_classes)
        n_imgs += 1

    print(f"  [{split}] {n_imgs} images, {n_boxes} boxes")
    return n_imgs, n_boxes


def write_data_yaml(names: list[str]) -> Path:
    """Write data.yaml that Ultralytics/YOLOv10 can consume."""
    yaml_path = DEST / "data.yaml"
    names_line = "[" + ", ".join(f"'{n}'" for n in names) + "]"
    content = (
        f"# Auto-generated by prepare_detection_dataset.py\n"
        f"path: {DEST.as_posix()}\n"
        f"train: train/images\n"
        f"val: valid/images\n"
        f"test: test/images\n"
        f"\n"
        f"nc: {len(names)}\n"
        f"names: {names_line}\n"
    )
    yaml_path.write_text(content)
    return yaml_path


def read_source_names(src_root: Path) -> list[str]:
    """Best-effort read of the original class names from the source data.yaml."""
    src_yaml = src_root / "data.yaml"
    if not src_yaml.exists():
        return []
    for line in src_yaml.read_text().splitlines():
        if line.strip().startswith("names:"):
            inside = line.split("[", 1)[-1].rstrip("]").strip()
            return [n.strip().strip("'\"") for n in inside.split(",") if n.strip()]
    return []


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--src", type=Path, default=DEFAULT_SRC,
                        help=f"Path to the Plate_v4 YOLOv8 folder. Default: {DEFAULT_SRC}")
    parser.add_argument("--keep-classes", action="store_true",
                        help="Keep the original 29 province classes instead of collapsing to 1.")
    parser.add_argument("--clean", action="store_true",
                        help="Delete data/annotated/ before copying (fresh start).")
    args = parser.parse_args()

    src_root: Path = args.src
    if not src_root.is_dir():
        raise SystemExit(f"ERROR: source folder not found: {src_root}")

    print(f"Source      : {src_root}")
    print(f"Destination : {DEST}")
    print(f"Mode        : {'keep 29 province classes' if args.keep_classes else 'collapse to 1 class (license_plate)'}")
    print("-" * 60)

    if args.clean and DEST.exists():
        print("Cleaning existing data/annotated/ ...")
        shutil.rmtree(DEST)

    total_imgs = 0
    total_boxes = 0
    for split in SPLITS:
        i, b = process_split(src_root, split, args.keep_classes)
        total_imgs += i
        total_boxes += b

    if total_imgs == 0:
        raise SystemExit("ERROR: no images copied — check the --src path/structure.")

    # Decide class names for the new data.yaml.
    if args.keep_classes:
        names = read_source_names(src_root) or [SINGLE_CLASS_NAME]
    else:
        names = [SINGLE_CLASS_NAME]

    yaml_path = write_data_yaml(names)

    print("-" * 60)
    print(f"DONE. {total_imgs} images, {total_boxes} boxes copied.")
    print(f"Classes     : {len(names)} -> {names if len(names) <= 5 else names[:5] + ['...']}")
    print(f"data.yaml   : {yaml_path}")
    print("\nNext: train the detector with")
    print("    python scripts/train_detector.py")


if __name__ == "__main__":
    main()
